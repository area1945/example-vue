    import Isotope from 'isotope-layout';
    import AOS from 'aos';
    import GLightbox from 'glightbox';
    import Swiper from 'swiper';
    import imagesLoaded from 'imagesloaded';
    import { Navigation, Pagination, Autoplay } from 'swiper/modules';

    const assetUrl = getCurrentInstance()?.appContext.config.globalProperties.$assetUrl
    const faqActive = ref<number>(0)
    const portfolioFilter = ref<string>("*")

    const handleClickFaq = (event: MouseEvent, index: number) => {
        event.preventDefault()
        faqActive.value = index
    }

    const handleClickMenu = (event: MouseEvent) => {
        event.preventDefault()
        const element = event.target as HTMLElement;
        const href = element.getAttribute('href');
        if(href){
          const target = document.querySelector(href);
          if(target){
            target?.scrollIntoView({ behavior: 'smooth' });
          }
        }
    }

    const handlePortfolioClick = (event: MouseEvent, filter:string) => {

        event.preventDefault()
        portfolioFilter.value = filter

        const container = document.querySelector('.isotope-container') as HTMLElement | null;
        let iso: Isotope | null = null;

         if (container) {
            // Initialize Isotope once
            iso = new Isotope(container, {
                itemSelector: '.isotope-item',
                layoutMode: 'fitRows',
                transitionDuration: '0.4s', // smooth animation
            });

            // Wait until all images are fully loaded
            imagesLoaded(container, () => {
                iso?.layout();
            });

            // Add event listener for filter buttons
            document.querySelectorAll('[data-filter]').forEach(button => {
                button.addEventListener('click', e => {
                e.preventDefault();
                const filter = (e.target as HTMLElement).getAttribute('data-filter') || '*';

                    // Always relayout before filtering
                    imagesLoaded(container, () => {
                        iso?.arrange({ filter });
                        iso?.layout();
                    });

                    // Optional: toggle active class on button
                    document.querySelectorAll('[data-filter]').forEach(btn => btn.classList.remove('active'));
                    (e.target as HTMLElement).classList.add('active');
                });
            });
        }

    }

    onMounted(() => {
        setTimeout(() => { 

        AOS.init({
            duration: 600,
            easing: 'ease-in-out',
            once: true,
            mirror: false
        });

        const lightbox = GLightbox({ selector: '.glightbox' });

        // Initialize Swiper
        const swiperClient = new Swiper('.swiper-client', {
            modules: [Navigation, Pagination, Autoplay],
            loop: true,
            speed: 600,
            autoplay: {
                delay: 5000
            },
            slidesPerView: 'auto',
            pagination: {
                el: '.swiper-pagination',
                type: 'bullets',
                clickable: true
            },
            breakpoints: {
                320: {
                    slidesPerView: 2,
                    spaceBetween: 40
                },
                480: {
                    slidesPerView: 3,
                    spaceBetween: 60
                },
                640: {
                    slidesPerView: 4,
                    spaceBetween: 80
                },
                992: {
                    slidesPerView: 6,
                    spaceBetween: 120
                }
            }
        });

       
        
        // Initialize Isotope
        const gridElement = document.querySelector('.isotope-container') as HTMLElement;
        if (gridElement) {
            new Isotope(gridElement, {
                itemSelector: '.isotope-item',
                layoutMode: 'masonry',
                filter: portfolioFilter.value,
                sortBy: 'original-order'
            });
        }

        // imagesLoaded example
        imagesLoaded('.isotope-container', () => {
            console.log('Images loaded!');
        });


        }, 500)
    })

</script>
